const popBox = (img, rating, context, compliments) => {
    // 1) Creating Board Panel
    
    // defining variables for main panel by creating the new element
    const popPanel = document.createElement("div");
    popPanel.classList.add("pop-box-panel");
    // close btn
    const closeButton = document.createElement("button");
    closeButton.type = "button";
    closeButton.classList.add("close-btn", "no-appearance-btn");
    // icon
    const closeIcon = document.createElement("img");
    closeIcon.src = "./icons/x-symbol-svgrepo-com.svg";
    closeIcon.alt = "A close icon, resembling the letter X";
    closeButton.appendChild(closeIcon) // append close icon to close button
    // overlay semi-transparent
    const overlayDrop = document.createElement("div");
    overlayDrop.classList.add("overlay-drop");

    // 2) compliment making procedures
    let numeralStamp = 0;
    let fullListCompliment = "";

    // 3) Gather each compliments into legible HTML structured list encapsulated to unordered list,
    //    but requires to verify if it have lists of compliments, which avoids throwing the error.
    if (compliments) {
        compliments.forEach((co) => {
            numeralStamp++;
            fullListCompliment += `
                <li class="card-compliment" id="cpt-${numeralStamp}">
                    ${co.icon ? co.icon : "<svg><!-- Blank SVG --></svg"}
                    <!-- Text Compliments: Special, Brief -->
                    <span class="special">${co.special}</span>
                </li>`;
        })
    }

    // 4) adding necessary event listeners
    closeButton.addEventListener("click", () => {
        popPanel.remove();
        overlayDrop.remove();
    })

    overlayDrop.addEventListener("click", () => {
        popPanel.remove();
        overlayDrop.remove();
    })

    // 5) creating based-structure HTML
    popPanel.innerHTML += `
        <!-- (I) Food Image Content -->
        <img src="${img.src}" alt="${img.alt}" class="fixed-asp">
        <!-- (II) Container Wraps -->
        <div class="container-wraps">
            <div class="food-text-description">
                <h2>${context.heading.textContent}</h2>
                <span class="list-rating">${rating.textContent}</span>
                <!-- Compliment Wrapper -->
                <ul class="compliment-container">
                ${fullListCompliment}
                </ul>
                <p>${context.par.textContent}</p>
            </div>
            <button type="button" class="emphasis-btn">Write a Review...</button>
        </div>
        <!-- (III) Absolute Close Button -->
    `;

    // 6) It's appending time
    // appending button (comes for the second of the last)
    popPanel.appendChild(closeButton);
    
    // appending button (comes for the last after the button)
    document.querySelector("body").appendChild(overlayDrop);

    // appending the panel
    document.querySelector("body").appendChild(popPanel);
}

